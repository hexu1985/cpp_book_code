#include <vector>
#include <algorithm>
#include <functional>
#include <cstdlib>
#include <iostream>

using namespace std;

template <class type>
void print(vector<type> vec)//打印函数
{
	vector<type>::iterator iter;
	for(iter=vec.begin();iter!=vec.end();++iter)
		cout<<*iter<<" ";
}

int main( )
{
   int a[7]={39,34,33,10,14,76,11};
   //初始化
vector <int> v1(a,a+7);
   int total;
  
   cout <<"The vector v1=(" ;
   print(v1);
   cout <<")"<< endl;   

   //应用bind2nd计算小于20的元素个数
   total=count_if( v1.begin( ), v1.end( ), 
   bind2nd(less<int>( ),20 ) );

   cout<<"The number of elements in v1 less than 20 is: "
       <<total<<endl;  //显示结果

   return 1;
}
